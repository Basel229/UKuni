		      	<p style="font-size: 17px;">Canada offers a number of express entry programs available to foreign entrepreneurs and investors wishing to start their own business in Canada, or wishing to accept a job offer to work in Canada. </p>
		<p style="font-size: 17px;">UKuni has helped many people start a new life in Canada and contribute to the Canadian economy. Please explore the programs listed below and contact us through the application assessment form. </p>
		<p style="font-size: 17px;">Express Entry manages applications for three economic immigration programs: </p>
		<ul>
			<li>Federal Skilled Worker Program</li>
			<li>Federal Skilled Trades Program</li>
			<li>Canadian Experience Class</li>
		</ul>
		<p style="font-size: 17px;">Provinces and territories can also recruit candidates from the Express Entry pool through the Provincial Nominee Program (PNP) to meet local labor market needs.</p>
		<p style="font-size: 30px;color:#eb1e25;">Provincial Nominee Programs</p>
		<p style="font-size: 17px;">Canada’s Federal Government has signed separate agreements with each of it’s provinces and northern territories regarding immigration. According to these agreements, the provinces and the territories have the right to select and nominate individuals to make them eligible to apply for immigration.</p>
		<p style="font-size: 17px;">The provinces and the territories have the ability to determine the criteria for the nominee selection. However, Canada’s Federal government still has authority over processing immigration applications. Consequently, the province’s authority is limited to granting the Provincial Nominee Certificate. The Federal Government will still have authority over examining and making decisions on immigration applications. Put simply, the Provincial Nominee Certificate is a document that makes the individual eligible to apply for immigration.</p>
		<p style="font-size: 30px;color:#eb1e25;">Federal Skilled Worker (Express Entry)</p>
		<p style="font-size: 17px;">This program is for skilled workers with foreign work experience who want to immigrate to Canada permanently.</p>
		<p style="font-size: 17px;">This program has minimum requirements for:</p>
		<ul>
			<li>skilled work experience</li>
			<li>language ability</li>
			<li>education</li>
		</ul>
		<p style="font-size: 17px;">You must meet all the minimum requirements to be eligible.</p>
		<p style="font-size: 30px;color:#eb1e25;">Selection factors</p>
		<p style="font-size: 17px;">If you meet all the minimum requirements, we’ll then assess your application based on:</p>
		<ul>
			<li>Age</li>
			<li>Education</li>
			<li>Working Experience</li>
			<li>whether you have a valid job offer</li>
			<li>English and/or French language skills</li>
			<li>adaptability (how well you’re likely to settle here)</li>
		</ul>
		<p style="font-size: 17px;">These factors are part of a 100-point grid used to assess eligibility for the Federal Skilled Worker Program. You earn points for how well you do in each of the 6 factors.</p>
		<p style="font-size: 17px;">The current pass mark is 67 points.</p>



		<p style="font-size: 30px;color:#eb1e25;">Federal Skilled Trades (Express Entry)</p>
		<p style="font-size: 17px;">The Federal Skilled Trades Program is for skilled workers who want to become permanent residents based on being qualified in a skilled trade.</p>
		<p style="font-size: 17px;">To be eligible, you must</p>
		<ul>
			<li>Meet the required language levels for each language ability</li>
			<ul>
				<li>writing</li>
				<li>reading</li>
				<li>listening</li>
				<li>speaking</li>
			</ul>
			<li>Have at least 2 years of full-time work experience (or an equal amount of part-time work experience) in a skilled trade within the 5 years before you apply</li>
			<li>meet the job requirements for that skilled trade as set out in the National Occupational Classification, except for needing a certificate of qualification</li>
			<li>Have a</li>
			<ul>
				<li>valid job offer of full-time employment for a total period of at least 1 year or</li>
				<li>certificate of qualification in that skilled trade issued by a Canadian provincial, territorial or federal authority</li>
				
			</ul>
		</ul>



		<p style="font-size: 30px;color:#eb1e25;">Canadian Experience Class (Express Entry)</p>
		<p style="font-size: 17px;">The Canadian Experience Class is for skilled workers who have Canadian work experience and want to become permanent residents.</p>
		<p style="font-size: 17px;">You must</p>
		<ul>
			<li>Meet the required language levels needed for your job for each language ability</li>
			<ul>
				<li>writing</li>
				<li>reading</li>
				<li>listening</li>
				<li>speaking</li>
			</ul>
			<li>Have at least 1 year of skilled work experience in Canada, in the last 3 years before you apply. The work experience must be:</li>
			<ul>
				<li>full-time or</li>
				<li>an equal amount in part-time</li>
			</ul>
			<li>Have gained your work experience by working in Canada legally</li>
			
		<p style="font-size: 17px;">According to the Canadian National Occupational Classification, skilled work experience means:</p>
		<ul>
			<li>managerial jobs (skill level 0)</li>
			<li>professional jobs (skill type A)</li>
			<li>technical jobs and skilled trades (skill type B)</li>
		</ul>
		<p style="font-size: 17px;">Your work experience can be in 1 or more NOC 0, A or B jobs.</p>
		<p style="font-size: 17px;">You must show that you performed the duties set out in the lead statement of the occupational description in the National Occupational Classification. This includes all the essential duties and most of the main duties listed.</p>
		

	
