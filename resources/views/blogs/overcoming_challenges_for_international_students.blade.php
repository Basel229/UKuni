		<br><br>
		<p style="font-size: 25px; text-align: left;color: #1f0949;">4. Finances</p>
		<p style="font-size: 17px;">Keeping up with your finances, be it earnings or savings, is another major thing to focus on. One of my very good friends here has a Funds Transfer Scheme (FTS) so he receives money every month, but at the end of each month he is still in debt. Even if you have heaps of money in your accounts I would suggest keeping records of your wasteful expenditure and try to bring it down (that’s a lesson for life). Earnings are kind of tough in New Zealand. Getting a job might not be that difficult but keeping a hold of it could be. Keep looking for opportunities and never miss one. Everything that helps you earn money is important. However, if you can, it is definitely sensible to choose a job which gives you a better future over a job with better money. </p>

