		<p style="font-size: 25px;">3. There Are Only 8 Universities in New Zealand</p>
		<p style="font-size: 17px;">Given its size, you might be surprised to learn that there are only eight universities in the whole country, expanding across the two islands.</p>
		<p style="font-size: 17px;">Students looking to immerse themselves in a "state university" type feel similar to that of the Big 10 in the U.S. can choose from Auckland University of Technology, University of Auckland, Massey University, University of Otago, or Victoria University of Wellington. These universities range from 17,000 - 33,000 students.</p>
		<p style="font-size: 17px;">For more of a private university feel, students can consider Lincoln University, University of Waikato, or University of Canterbury, ranging from 3,000 - 12,000 students.</p>
		
