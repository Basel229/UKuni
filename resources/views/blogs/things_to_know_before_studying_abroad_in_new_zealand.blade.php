		<p style="font-size: 25px;">1. You'll Need to Pack for Chilly Weather</p>
		
		<p style="font-size: 17px">The thing about New Zealand is that oftentimes you can experience all four seasons in a day. When preparing for your journey to the "land of the long white cloud", make sure to pack sweaters, a rain jacket, and layered everyday wear.
		As it's situated in one the most mountainous regions in the world, expect to experience weather that typically ranges from the 50s to low 70s throughout the year, with a chance of sporadic rain. New Zealand weather also tends to be cooler in the south than in the north, and the coldest time of the year there is between June and August.
		Something important to keep in mind is that that the higher the altitude, the cooler the temperature, which means you'll need to pack warmer clothing for your hikes and outdoor activities outside of the city limits.</p>

