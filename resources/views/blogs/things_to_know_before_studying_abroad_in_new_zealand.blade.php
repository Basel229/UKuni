		<p style="font-size: 25px;">4. The Cities Are Bustling</p>
		<img src="images/blogs/nz7.jpg" style="width: 70%;">
		<p style="font-size: 17px;">Wellington, the capital of New Zealand, is the southern-most capital city in the world. Here, you'll find the government headquarters like the Parliament and Supreme Court, as well as many big businesses, universities, and cultural hubs such as museums, operas, ballets, and archives.</p>
		<p style="font-size: 17px;">Auckland, located on the North Island is the biggest city in New Zealand. To date, Auckland also has the largest Polynesian population in the world. A city of two harbors, surrounded by hilly rainforests, Auckland was recently named one of the world's most livable cities, according to the Economist's World's Most Livable Cities index from 2015.</p>
		<p style="font-size: 17px;">Of course, there are other cities in New Zealand to visit or study in, including Christchurch in Canterbury, Hamilton in Waikato, Tauranga in Bay of Plenty, and Napier-Hastings in Hawk's Bay.</p>

